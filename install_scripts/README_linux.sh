# Environment Installation Guide

## Packages Install
- [X] Install Apt Packages
  ```
  sudo apt update
  sudo ./install_apt_packages.sh
  ```
  
- [X] Install Dot Files
  ```
  sudo ./install_dotfiles.sh
  ```

- [X] Symlink Dot Files
  ```
  ./install_symlinks.sh
  ```

- [X] Install Command Line Applications
  ```
  sudo ./install_cli_apps.sh
  ```


